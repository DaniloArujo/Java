public class Aula002TiposPrimitivos {
    public static void main(String[] args) {
        //int, double, float, char, byte, short, long, boolean

        int num = 10;
        System.out.println("num = "+ num);

        double duplo = 10.10;
        System.out.println("Double"+ duplo);
        //o float precisa do casting float duplo = 10.10f;

        char letra = 'D';
        System.out.println("Char: "+ letra);

        String nome = "Danilo";
        System.out.println(nome);

        byte exbyte = 3;
        System.out.println("Byte: "+exbyte);

        short exshort = 22;
        System.out.println("Short: "+exshort );

        long exlong = 3000;
        System.out.println("Long: "+ exlong);

        boolean exboolean = true;
        System.out.println("boolean: "+ exboolean);



    }
}
